          <br><div align="justify"><b>Sony SonicStage (ATRAC)</b></div><p></font>
        <font face="Tahoma" size=2 color="black"><blockquote>
        <a href="screens/sonicstage.png" target=outside><img src="screens/sonicstage.png" 
        alt="Sony Sonic Stage" align=right height=320></a>
        SonicStage is an audio library manager created by Sony to work with their
        MiniDisc devices. The installation is bloated, takes forever and installs 
        all kinds of drivers and barely-related crap on your Windows. The user 
        interface is also quite unfriendly (that being said, 4.3 seems friendlier 
        than 2.0 in the limited tests I did).<br><br>
        
        But I did not create this page to complain about SonicStage. I am here to 
        talk about ATRAC, which is Sony's audio compression format, and SonicStage
        just happens to be the encoder.<br><br>
        
        ATRAC - Adaptive Transform Acoustic Coding - started being developed by 
        Sony in the early nineties as an audio data reduction technique to be 
        employed in their movie theater audio system - SDDS (Sony Dynamic Digital 
        Sound) - that encoded 7.1 audio streams in 1168kbps and stored the data 
        optically next to the film stock sprockets. In this capacity, it competed 
        with the <a href=softencode.php>Dolby AC3</a> and DTS formats.<br><br>
        
        Around the same time, Sony was working on a magneto-optical media storage
        system called MiniDisc that used ATRAC to compress audio, reducing CD
        quality streams from 1411kbps (uncompressed) to 292kbps - or 146kbps in
        mono mode. There are several backwards- and forwards-compatible versions
        of the ATRAC encoder created by Sony, from v1 to v4.5, and it is said it 
        only started sounding good in version 3. Other MiniDisc manufacturers 
        such as Panasonic and Sharp also released their own tuned versions of 
        the encoder.<br><br>
        
        In 1999 Sony launched the first major upgrade to the ATRAC format with 
        the release of ATRAC3. It was launched as a response to the perceived 
        technological superiority of the MP3 format (similar quality at much 
        smaller bitrates), so ATRAC3 claims to achieve transparency in LP2 mode, 
        at 132kbps. There is also an LP4 mode that stores tracks in 66kbps but 
        with noticeable quality degradation. While being based on ATRAC, it is 
        not backwards compatible as it includes several coding improvements and 
        modified algorithms - therefore it will not work with old MiniDisc 
        players, only with newer MDLP (MiniDisc Long Play) devices.<br><br>
        
        The next iteration of the format was called ATRAC3plus and launched in 
        2003, still based on the same underlying technologies but with further 
        backwards-incompatible improvements. It offered bitrates ranging from 48
        to 352kbps and was only compatible with Hi-MD MiniDisc devices (but also
        compatible with several other Sony devices such as VAIO cell phones, PS3
        and PSP consoles, and Xplod car players).<br><br>
        
        A parallel iteration of the format is ATRAC Advanced Lossless, in which 
        an audio stream is encoded in ATRAC3 or ATRAC3plus and the "discarded"
        audio data is losslessly compressed and merged to the lossy stream. That
        way, players that understand the extra data can play lossless audio,
        and older players that do not can play the normal lossy stream. It is 
        the same idea implemented by <a href=fhgmp3hd.php>mp3HD</a>, <a 
        href="wavpack.php">WavPack</a> hybrid mode and OptimFrog DualStream.<br><br>
        
        The latest iteration of the format is called ATRAC9, and is focused on 
        video game audio for Sony consoles.<br><br>
        
        Besides being implemented in several hardware devices and software 
        programs created by Sony, the format has been licensed to other MiniDisc
        manufacturers such as Sharp and Panasonic (as mentioned above), to Real 
        Networks as the audio compression engine in <a href=realjuke.php>Real 
        Audio 8</a>, and to <a href=liquid.php>Liquid Audio<a/> as a format 
        option in their platform</a>.<br><br>
        
        You can also try the Sony ATRAC3 ACM codec, available below. It integrates 
        with the Windows <a href=acmstation.php>Audio Compression Manager</a> and
        encodes to ATRAC3 files wrapped in the WAV container, allowing bitrates 
        of 66, 105 and 132kbps. Keep in mind this is quite old code and probably
        does not reflect the best quality ATRAC3 can provide.<br><br>
        
        None of the ATRAC versions and iterations has ever been publicly documented,
        which in part explains the small popularity the format enjoys outside the
        Sony world. There is no VBR encoding at all and no multichannel 
        implementation other than the completely proprietary SDDS. But the FFMPEG
        project reverse engineered and provides decoders for all iterations of 
        ATRAC in their libavcodec library.<br><br>
        
        The official ATRAC home page at Sony's web site is available <a 
        target=outside href="https://www.sony.net/Products/ATRAC3/">here</a> and
        you can see a mirror of the SDDS website at the <a target=outside 
        href="https://web.archive.org/web/20130701055758/http://www.sdds.com/">
        Internet Archive</a>.<br>

